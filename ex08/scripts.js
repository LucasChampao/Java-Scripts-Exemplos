const key= "Lucas"
// IF (SE SENÃO)
if(key == 'Lucas'){
    console.log(`Olá ${key}, seja bem vindo !!`)
}else{
    console.log(`dados inválido!!`)
}
// ESCOLHA (SWITCH)
switch (key) {
    case 'Joao':
        console.log(`Olá ${key}, Bem vindo! :D`)
        break;
    case 'Lucas':
        console.log(`Òlá ${key}, Bem vindo :D!`)
        break;
    default:
        console.log('nome não encontrado !!')
        break;
        
}
